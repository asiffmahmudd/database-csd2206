/* 7 */
SELECT FIRST_NAME || ' ' || LAST_NAME AS "EMPLOYEE", MONTHLY_SALARY *12 AS "CURRENT_SALARY", TO_CHAR(MONTHLY_SALARY*12*1.08125, '999999D99') AS "NEW_SALARY" 
FROM B_EMPLOYEES
WHERE MONTHLY_SALARY *12*1.08125 > 75000
ORDER BY NEW_SALARY DESC;

/* 8 */
SELECT CITY, CUSTOMER_NAME, ROUND(CREDIT_LIMIT,2) AS "CURRENT_CREDIT_LIMIT", DECIMAL(CREDIT_LIMIT * 1.0525,7,2) AS "NEW_CREDIT_LIMIT"
FROM B_CUSTOMERS
WHERE CREDIT_LIMIT * 1.0525 BETWEEN 60000 AND 75000
ORDER BY CITY, CREDIT_LIMIT;

/* 9 */
SELECT PRODUCT_CODE, PRICE AS "ORIGINAL_PRICE", DECIMAL(FLOOR(PRICE),5,2) AS "MONDAY_PRICE", DECIMAL(CEILING(PRICE),5,2) AS "TUESDAY_PRICE"
FROM B_PRICES;

