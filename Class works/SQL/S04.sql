/* 3 */
SELECT FIRST_NAME || ' ' || LAST_NAME AS "EMPLOYEE", MONTHLY_SALARY *12 AS "YEARLY_SALARY"
FROM B_EMPLOYEES
WHERE MONTHLY_SALARY *12 > 75000
ORDER BY YEARLY_SALARY DESC;

/* 4 */
SELECT FIRST_NAME || ' ' || LAST_NAME AS "EMPLOYEE", DECIMAL(MONTHLY_SALARY *12,8,2) AS "CURRENT_SALARY", MONTHLY_SALARY *12*1.08125 AS "NEW_SALARY" 
FROM B_EMPLOYEES
WHERE MONTHLY_SALARY *12*1.08125 > 75000
ORDER BY NEW_SALARY DESC;

/* 5 */
